def substract(num1,num2):
    """"Can substract num1 from num2"""
    return num1-num2

def add(num1,num2):
    """"Can add num1 and num2"""
    return num1+num2

def multiply(num1,num2):
    """"Can multiply num1 and num2"""
    return num1*num2

def divide (num1,num2):
    """"Can divide num1 by num2"""
    return num1/num2
